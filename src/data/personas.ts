export interface ResolvedPersona {
  key: string;
  profile: PersonaProfile;
}

export function resolvePersona(
  rawCode: string,
  progress: DBTIProgress,
  totals: DimensionTotals
): ResolvedPersona {
  if (rawCode === "MASL") {
    const altruism = totals.Altruistic - totals.Egoistic;
    const layFlat = totals.Resigned - totals.Tenacious;
    if (layFlat > altruism && progress.W_vs_L > 0) {
      return { key: "MASL_SLIPPERY", profile: personas.MASL_SLIPPERY };
    }
    return { key: "MASL", profile: personas.MASL };
  }

  const profile = personas[rawCode];
  if (profile) return { key: rawCode, profile };

  return { key: "MCBW", profile: personas.MCBW };
}
